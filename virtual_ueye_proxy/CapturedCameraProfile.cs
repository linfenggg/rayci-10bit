    internal static IEnumerable<KeyValuePair<int, ushort>> EnumerateStartupWords()
    {
        foreach (var pair in StartupRegisterValues.OrderBy(pair => pair.Key))
        {
            yield return new KeyValuePair<int, ushort>(pair.Key, unchecked((ushort)(pair.Value & 0xffffu)));
            yield return new KeyValuePair<int, ushort>(pair.Key + sizeof(ushort), unchecked((ushort)(pair.Value >> 16)));
        }
    }

    private static byte[] Hex(string hex) => Convert.FromHexString(hex);
